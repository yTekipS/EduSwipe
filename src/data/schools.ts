const normalize = (text: string): string =>
  text
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/ł/g, 'l');

const inferRegion = (location: string): 'podkarpackie' | 'other' => {
  const normalizedLocation = normalize(location);
  const hasPodkarpackieMention = normalizedLocation.includes('podkarpac');
  if (hasPodkarpackieMention) {
    return 'podkarpackie';
  }

  const tokens = normalizedLocation
    .split(/[\/(),-]/)
    .map((part) => part.trim())
    .filter(Boolean);

  const isPodkarpackie = tokens.some((token) => PODKARPACKIE_CITIES.has(token));
  return isPodkarpackie ? 'podkarpackie' : 'other';
};

const withRegion = (schools: School[]): School[] =>
  schools.map((school) => ({
    ...school,
    region: school.region ?? inferRegion(school.location),
  }));

const dedupeSchools = (schools: School[]): School[] => {
  const seen = new Set<string>();
  return schools.filter((school) => {
    const key = `${school.type}|${normalize(school.name)}|${normalize(school.location)}`;
    if (seen.has(key)) {
      return false;
    }
    seen.add(key);
    return true;
  });
};

